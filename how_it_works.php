		<p>This website is meant to be considered in conjunction with our physical mini-closet prototype and StoryLamp prototype.</p> 
		<h2><b>RFID Tagged Clothing Items</b></h2>
		<font align = "center">
		<img class="displayed" src="images/rfid.jpg" align="middle">
		</font>
		<br>
		<p>Minimize is designed for a future in which RFID tags are already embedded within clothing items at the point of production. When a user purchases or receives a new item of clothing, they can either scan it to their closet or manually add it to their account on the <a href="http://s4325075-minimalist.uqcloud.net/development.html">My Closet</a> page. Each day, users scan the items they decide to wear. Over time personal wearing data will be collected, forming a larger picture of the user's clothing habits. This information is then meaningfully presented back to the user in both digital and tangible ways.</p>
		<h2><b>Closet Lights</b></h2>
		<font align = "center">
		<img class="displayed" src="images/closet.jpg" align="middle">
		</font>
		<br>
		<p>There are LED lights above each item. These lights allow users to individually visualize two sets of data:</p>
		<p><b>The Number of Days Ago the Item was Last Worn.</b> Visualizing this data can aid users in easily recognizing the items they have neglected, which can prompt them to either use those items more or get rid of them so that they can be of use to someone else.</p>
		<br>
		<font align = "center">
		<img class="displayed" src="images/lastworn.jpg" align="middle">
		</font>
		<br>
		<br>
		<p><b>The Appropriateness of the Item Given the Current Temperature.</b> Visualizing this data can help users identify items to temporarily store away when they are not in season. (This is very similar to the concept of "capsule wardrobes.")</p>
		<br>
		<font align = "center">
		<img class="displayed" src="images/temperature.jpg" align="middle">
		</font>
		<br>
		<p><b>Button.</b> Pressing the button switches the data the lights display from one of the above datasets to the other.</p>
		<font align = "center">
		<img class="displayed" src="images/button.jpg" align="middle">
		</font>	
		<h2><b>Closet Door</b></h2>
		<p><b>Goal Progress. </b>On the closet door is a NeoPixel ring that allows users to visualize their progress towards meeting the numeric goal they set on the <a href="http://s4325075-minimalist.uqcloud.net/goal">Goals</a> page.</p>
		<p><b>Neglected Items. </b>The closet door also displays images of items that the user hasn't worn in over 100 days in addition to the number of days ago since those items were last worn. This serves as a constant visual reminder to encourage users to minimize.</p>
		<br>
		<img class="displayed" src="images/doors.jpg" align="middle">
		<h2><b>StoryLamp</b></h2>
		<p>The purpose of StoryLamp is to assist users in parting with unused sentimental clothing items. Some early feedback we received on our project indicated that even seeing the extent to which items have been neglected may not necessarily help users minimize if they experience attachment to their clothing items (which is particularly common among hoarders). StoryLamp was built in response to this feedback.</p>
		<font align = "center">
		<img class="displayed" src="images/storylamp.jpg" align="middle">
		</font>
		<br>
		<p>As Joshua Fields Millburn of The Minimalists writes, "An item that is <i>sentimental</i> for us can be <i>useful</i> for someone else."</p>
		<p>StoryLamp is designed to feature dynamic images associated with the root of the discarded items&#39; sentimentality, which allows users to preserve memories and retain the identity those items bestowed upon them. RFID technology enables the lamp to glow when one of the user&#39;s donated items is being worn, positively reinforcing the user&#39;s decision to part with the item.</p>
		<p>Users can select the StoryLamp's images by uploading images of their own to the <a href="http://s4325075-minimalist.uqcloud.net/discarded_items.php">Discarded Items</a> page. In addition, users are able add stories to each item that capture the significance that item held. Various popular blogs on minimalism recommend storytelling as device that can assist people in letting go of sentimental items.</p>
		

