		<p/><b>The Number of Days Ago the Item was Last Worn.</b> Our display allows users to visualize items they are neglecting by featuring light over each item in a color that corresponds with the number of days ago the item was last worn. Scanning an item to the RFID reader (which users are instructed to do before wearing the item) will increment the number of times the item was worn and reset the number of days ago it was last worn (setting the light color above the item to white). Visualizing this data can aid users in easily recognizing the items they have neglected, which can prompt them to either use those items more or get rid of them so that they can be of use to someone else.</p>
		<p><b>The Appropriateness of the Item Given the Current Weather.</b> This can help users identify items to temporarily store away when they are not in season. (This is very similar to the concept of "capsule wardrobes.")</p>
